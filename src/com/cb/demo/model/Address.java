package com.cb.demo.model;

public class Address {
	
	private String stline1;
	private String stline2;
	private String city;
	private String state;
	private String zipcode;
	
	
	public String getStline1() {
		return stline1;
	}
	public void setStline1(String stline1) {
		this.stline1 = stline1;
	}
	public String getStline2() {
		return stline2;
	}
	public void setStline2(String stline2) {
		this.stline2 = stline2;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getZipcode() {
		return zipcode;
	}
	public void setZipcode(String zipcode) {
		this.zipcode = zipcode;
	}
	
	

}
